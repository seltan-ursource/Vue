<template>
    <div>
        <label v-if="label">{{label}}</label>
         <input :value="value" @input="updateValue" v-bind="$attrs"/>
    </div>
</template>

<script>
    export default {
        inheritAttrs: false,
        props:{
            label:{
                type : String,
                default : ''
            },
            value:[String, Number]
        },
        methods:{
            updateValue(product){
                this.$emit('input', product.target.value)
            }
        }
    }
</script>

